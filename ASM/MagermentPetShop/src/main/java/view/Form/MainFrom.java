    // Validate Pet
    public boolean validatePet() {

        StringBuffer str = new StringBuffer();
        boolean flag = true;

        //Id
        if (Validate.isEmpty(txtIdPet)) {
            str.append("ID cannot be blank ! \n");
            flag = false;
        } else {
            if (!Validate.isInt(txtIdPet)) {
                str.append("ID must be numeric ! \n");
                flag = false;
            }
        }

        // Name
        if (Validate.isEmpty(txtNamePet)) {
            str.append("Name cannot be blank ! \n");
            flag = false;
        }

        // Age
        if (Validate.isEmpty(txtAgePet)) {
            str.append("Age cannot be blank ! \n");
            flag = false;
        } else {
            if (!Validate.isInt(txtAgePet)) {
                str.append("Age must be numeric ! \n");
                flag = false;
            }
        }

        //Weight
        if (Validate.isEmpty(txtWeightPet)) {
            str.append("Weight cannot be blank ! \n");
            flag = false;
        } else {
            if (!Validate.isFloat(txtIdPet)) {
                str.append("Weight must be numeric ! \n");
                flag = false;
            }
        }

        //Price
        if (Validate.isEmpty(txtPricePet)) {
            str.append("Price cannot be blank ! \n");
            flag = false;
        } else {
            if (!Validate.isFloat(txtPricePet)) {
                str.append("Price must be numeric ! \n");
                flag = false;
            }
        }

        // Color
        if (Validate.isEmpty(txtColorPet)) {
            str.append("Color cannot be blank ! \n");
            flag = false;
        }

        // Color
        if (Validate.isEmpty(txtTypePet)) {
            str.append("Type cannot be blank ! \n");
            flag = false;
        }

        //genther
        if (btgGentherPet.isSelected(null)) {
            str.append("You haven't chosen your pet's gender yet !");
            flag = false;
        }

        if (str.length() > 0) {
            Message.showWarning(str.toString(), this);
        }

        return flag;
    }

    // Validate Personal
    public boolean validatePersonal() {
        StringBuffer str = new StringBuffer();
        boolean flag = true;
        // Id
        if (Validate.isEmpty(txtIdPerson)) {
            str.append("Id cannot be blank ! \n");
            flag = false;
        }

        // Name
        if (Validate.isEmpty(txtNamePerson)) {
            str.append("Name cannot be blank ! \n");
            flag = false;
        }

        // Birth
        if (Validate.isEmpty(txtBirthPerson)) {
            str.append("Birth cannot be blank ! \n");
            flag = false;
        } else {
            try {
                XDate.parses(txtBirthPerson.getText());
            } catch (ParseException ex) {
                str.append("Invalid date of birth ! \n");
                flag = false;
            }
        }

        // Email
        if (Validate.isEmpty(txtEmailPerson)) {
            str.append("Email cannot be blank ! \n");
            flag = false;
        } else {
            if (!Data.isEmail(txtEmailPerson.getText())) {
                str.append("Invalid email \n");
                flag = false;
            }
        }

        //genther
        if (btgGentherPerson.isSelected(null)) {
            str.append("You haven't chosen your gender yet ! \n");
            flag = false;
        }

        //Role
        if (btgRolePerson.isSelected(null)) {
            str.append("You have not selected a position ! \n");
            flag = false;
        }
        if (str.length() > 0) {
            Message.showWarning(str.toString(), this);
        }

        return flag;
    }

